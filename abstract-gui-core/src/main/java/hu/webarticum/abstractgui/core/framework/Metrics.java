package hu.webarticum.abstractgui.core.framework;

public class Metrics {
    
    public final int x;
    
    public final int y;
    
    public Metrics(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
    
}
